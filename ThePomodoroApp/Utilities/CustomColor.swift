import UIKit
extension UIColor{

    class var dimmedPinkRed:UIColor{
        return UIColor(red: 228/255, green: 90/255, blue: 95/255, alpha: 1.0)
    }
    
    class var lightDimmedPinkRed: UIColor{
        return UIColor(red: 250/255,green: 176/255, blue: 163/255, alpha: 1.0)
    }
    
    class var MediumDimmedPinkRed: UIColor{
        return UIColor(red: 246/255,green: 143/255, blue: 144/255, alpha: 1.0)
    }
    
    class var MediumDarkGray:UIColor{
        return UIColor(red: 58/255, green: 60/255, blue: 63/255, alpha: 1.0)
    }
    
}
